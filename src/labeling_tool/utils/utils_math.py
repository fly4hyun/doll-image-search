import random
import string
from datetime import datetime





###################################################################################################
###################################################################################################

def generate_random_variable():
    date = datetime.now().strftime("%Y%m%d")  # 현재 날짜를 YYYYMMDD 형식으로
    letters = ''.join(random.choices(string.ascii_uppercase, k=4))  # 영어 대문자 4개
    number = random.randint(0, 9)  # 숫자 1개
    return f"a{date}{letters}{number}"  # aYYYYMMDDXXXXN 형식으로 반환



